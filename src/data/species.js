export function getSpeciesProfile(species) {
  return SPECIES_PROFILES[species]
}

export function getSpeciesCatalog() {
  return SPECIES.map(species => ({ species, ...SPECIES_PROFILES[species] }))
}

export function findSpecies(input) {
  const normalized = input.trim().toLowerCase()
  return SPECIES.find(species => {
    const profile = SPECIES_PROFILES[species]
    return profile.aliases.some(alias => alias.toLowerCase() === normalized)
  })
}
