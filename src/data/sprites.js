const HAT_LINES = {
  none: '',
  crown: '   \\^^^/    ',
  tophat: '   [___]    ',
  propeller: '    -+-     ',
  halo: '   (---)    ',
  wizard: '    /^\\     ',
  beanie: '   (___)    ',
  tinyduck: '    ,>      ',
}

export function renderSprite(pet, frame = 0) {
  const frames = BODIES[pet.species]
  const body = frames[frame % frames.length].map(line => line.replaceAll('{E}', pet.eye))
  const lines = [...body]

  if (pet.hat !== 'none' && !lines[0].trim()) {
    lines[0] = HAT_LINES[pet.hat]
  }

  if (!lines[0].trim() && frames.every(spriteFrame => !spriteFrame[0].trim())) {
    lines.shift()
  }

  return lines
}

export function spriteFrameCount(species) {
  return BODIES[species].length
}

export function renderFace(pet) {
  const eye = pet.eye
  switch (pet.species) {
    case 'duck':
    case 'goose':
      return `(${eye}>`
    case 'blob':
      return `(${eye}${eye})`
    case 'cat':
      return `=${eye}^${eye}=`
    case 'dragon':
      return `<${eye}~${eye}>`
    case 'octopus':
      return `~(${eye}${eye})~`
    case 'owl':
      return `(${eye})(${eye})`
    case 'penguin':
      return `(${eye}>)`
    case 'turtle':
      return `[${eye}_${eye}]`
    case 'snail':
      return `${eye}(@)`
    case 'ghost':
      return `/${eye}${eye}\\`
    case 'axolotl':
      return `}${eye}.${eye}{`
    case 'capybara':
      return `(${eye}oo${eye})`
    case 'cactus':
      return `|${eye}  ${eye}|`
    case 'robot':
      return `[${eye}${eye}]`
    case 'rabbit':
      return `(${eye}..${eye})`
    case 'mushroom':
      return `|${eye}  ${eye}|`
    case 'chonk':
      return `(${eye}.${eye})`
    default:
      return `(${eye}${eye})`
  }
}

export function renderSpeciesPreview(species) {
  if (!SPECIES.includes(species)) {
    return '(??)'
  }

  return renderFace({ species, eye: 'o' })
}
